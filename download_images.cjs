const outputDir = path.join(__dirname, 'public', 'images', 'portfolio');
if (!fs.existsSync(outputDir)) fs.mkdirSync(outputDir, { recursive: true });

let done = 0;
let failed = 0;

function download(imgUrl, idx) {
  return new Promise((resolve) => {
    const parsed = url.parse(imgUrl);
    const filename = path.basename(parsed.pathname);
    const dest = path.join(outputDir, filename);

    if (fs.existsSync(dest)) {
      done++;
      if (done % 20 === 0) console.log(`진행중: ${done}/${urls.length}`);
      return resolve();
    }

    const file = fs.createWriteStream(dest);
    const proto = imgUrl.startsWith('https') ? https : http;

    proto.get(imgUrl, (res) => {
      if (res.statusCode === 200) {
        res.pipe(file);
        file.on('finish', () => {
          file.close();
          done++;
          if (done % 20 === 0) console.log(`진행중: ${done}/${urls.length}`);
          resolve();
        });
      } else {
        file.close();
        fs.unlink(dest, () => {});
        failed++;
        resolve();
      }
    }).on('error', (e) => {
      file.close();
      fs.unlink(dest, () => {});
      failed++;
      resolve();
    });
  });
}

async function main() {
  console.log(`총 ${urls.length}개 이미지 다운로드 시작...`);
  
  // 10개씩 병렬 다운로드
  for (let i = 0; i < urls.length; i += 10) {
    const batch = urls.slice(i, i + 10);
    await Promise.all(batch.map((u, j) => download(u, i + j)));
  }
  
  console.log(`\n완료! 성공: ${done}개, 실패: ${failed}개`);
  console.log(`저장 위치: ${outputDir}`);
}

main();
